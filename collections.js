document.addEventListener('DOMContentLoaded', () => {
    // Get collection type from URL
    const params = new URLSearchParams(window.location.search);
    const collectionType = params.get("type");

    // Get elements
    const titleEl = document.getElementById("collection-title");
    const descEl = document.getElementById("collection-desc");
    const gridEl = document.getElementById("gallery-grid");

    if (gridEl) {
        // Clear loading text
        gridEl.innerHTML = "";

        // Validate collection
        if (!collectionType || !collectionsData[collectionType]) {
            if (titleEl) titleEl.textContent = "Collection Not Found";
            if (descEl) descEl.textContent = "Please go back and select a valid collection.";
            return;
        }

        const collection = collectionsData[collectionType];

        // Set title & description
        if (titleEl) titleEl.textContent = collection.title;
        if (descEl) descEl.textContent = collection.description;


        collection.images.forEach((item, index) => {
            const imgPath = item.src;
            const price = item.price;

            const itemDiv = document.createElement("div");
            itemDiv.className = "product-card";

            const displayName = `${collection.title} - Style ${index + 1}`;

            itemDiv.innerHTML = `
        <div class="product-img">
            <img src="${imgPath}" alt="${displayName}" loading="lazy">
            <button class="add-to-cart-btn" onclick="addToCart('${displayName}', ${price}, '${imgPath}')">
                Add to Cart
            </button>
        </div>
        <div class="product-info">
            <h4>${displayName}</h4>
            <p class="price">â‚¹${price}</p>
        </div>
    `;

            gridEl.appendChild(itemDiv);
        });

    }
});
